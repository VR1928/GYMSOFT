package com.apm.DiaryManagement.eu.entity;

public class GpData {
	
	private int id;
	
	private String gpName;
	
	private int tptypeid;
	
	private int tpid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGpName() {
		return gpName;
	}

	public void setGpName(String gpName) {
		this.gpName = gpName;
	}

	public int getTptypeid() {
		return tptypeid;
	}

	public void setTptypeid(int tptypeid) {
		this.tptypeid = tptypeid;
	}

	public int getTpid() {
		return tpid;
	}

	public void setTpid(int tpid) {
		this.tpid = tpid;
	}
	
	
	
	

}
